(X_train, _), _ = fashion_mnist.load_data()

# Preprocess
X_train = X_train.reshape(-1, 784) / 255.0

# Use smaller subset for fast training
X_train = X_train[:10000]

# -----------------------
# HYPERPARAMETERS
# -----------------------
input_size = 784
latent_size = 32
lr = 0.1
epochs = 15
lambda_reg = 0.001   # Sparsity strength

# -----------------------
# INITIALIZE WEIGHTS
# -----------------------
W1 = np.random.randn(input_size, latent_size) * 0.01
b1 = np.zeros((1, latent_size))

W2 = np.random.randn(latent_size, input_size) * 0.01
b2 = np.zeros((1, input_size))

# -----------------------
# ACTIVATION
# -----------------------
def sigmoid(z):
    return 1 / (1 + np.exp(-z))

# -----------------------
# TRAINING LOOP
# -----------------------
losses = []

for epoch in range(epochs):

    # Forward pass
    z1 = X_train @ W1 + b1
    a1 = sigmoid(z1)          # Latent representation

    z2 = a1 @ W2 + b2
    output = sigmoid(z2)      # Reconstruction

    # Sparse Loss (MSE + L1 penalty on latent layer)
    mse_loss = np.mean((X_train - output) ** 2)
    sparse_loss = lambda_reg * np.mean(np.abs(a1))
    loss = mse_loss + sparse_loss

    losses.append(loss)

    # Backpropagation
    m = X_train.shape[0]

    dz2 = (output - X_train) * output * (1 - output)
    dW2 = a1.T @ dz2 / m
    db2 = np.sum(dz2, axis=0, keepdims=True) / m

    dz1 = (dz2 @ W2.T) * a1 * (1 - a1)
    dW1 = X_train.T @ dz1 / m
    db1 = np.sum(dz1, axis=0, keepdims=True) / m

    # Update
    W1 -= lr * dW1
    b1 -= lr * db1
    W2 -= lr * dW2
    b2 -= lr * db2

    print(f"Epoch {epoch+1}, Loss: {loss:.6f}")

# -----------------------
# PLOT LOSS CURVE
# -----------------------
plt.plot(losses)
plt.title("Sparse Autoencoder Reconstruction Loss")
plt.xlabel("Epoch")
plt.ylabel("Loss")
plt.show()

