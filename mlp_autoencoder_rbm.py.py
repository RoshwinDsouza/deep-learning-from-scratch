import numpy as np
import matplotlib.pyplot as plt
from tensorflow.keras.datasets import fashion_mnist

# Load dataset
(X_train, _), _ = fashion_mnist.load_data()
X_train = X_train.reshape(-1, 784) / 255.0

# Use smaller subset for speed
X_train = X_train[:10000]

visible = 784
hidden = 100
lr = 0.01
epochs = 10

# Initialize weights
W = np.random.randn(visible, hidden) * 0.01

def sigmoid(x):
    return 1 / (1 + np.exp(-x))

errors = []

for epoch in range(epochs):

    # Positive phase
    v0 = X_train
    h0 = sigmoid(v0 @ W)

    # Negative phase (reconstruction)
    v1 = sigmoid(h0 @ W.T)
    h1 = sigmoid(v1 @ W)

    # Contrastive Divergence update
    W += lr * (v0.T @ h0 - v1.T @ h1) / X_train.shape[0]

    error = np.mean((v0 - v1) ** 2)
    errors.append(error)

    print(f"Epoch {epoch+1}, Reconstruction Error: {error:.6f}")

plt.plot(errors)
plt.title("RBM Reconstruction Error")
plt.xlabel("Epoch")
plt.ylabel("Error")
plt.show()